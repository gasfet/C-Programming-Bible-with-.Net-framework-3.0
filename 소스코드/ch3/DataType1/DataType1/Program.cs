class DataType1
{
    public static void Main()
	{
		// 변수 초기화
	    byte b = 10 ;     // 10진수
		short s = 010 ;   // 8진수
		int i = 0x10 ;    // 16진수    
		long l = 10 ;
        
		System.Console.WriteLine("byte  :" + b);
		System.Console.WriteLine("short :" + s);
     	System.Console.WriteLine("int   :" + i);
        System.Console.WriteLine("long  :" + l);
	}
}
