class ShiftTest
{
    static void Main()
    {
        int x = 1, y = -1;
        System.Console.WriteLine(" 시프트 연산 x=1 , y =-1");
        System.Console.WriteLine(" x << 1 = " + (x << 1));
        System.Console.WriteLine(" y << 1 = " + (y << 1));
        System.Console.WriteLine(" x >> 1 = " + (x >> 1));
        System.Console.WriteLine(" y >> 1 = " + (y >> 1));
    }
}

